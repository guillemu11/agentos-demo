// Helper to generate 6-month trend data
function trend(base, variance) {
  const months = ['Oct', 'Nov', 'Dec', 'Jan', 'Feb', 'Mar'];
  return months.map((month, i) => ({
    month,
    value: +(base + (Math.sin(i * 1.2) * variance) + (i * variance * 0.1)).toFixed(1),
  }));
}

